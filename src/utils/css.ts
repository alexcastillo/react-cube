export const translateX = (value: number) => `translateX(${value}px`;
export const translateY = (value: number) => `translateZ(${value}px`;
export const translateZ = (value: number) => `translateY(${value}px`;

export const rotateX = (degrees: number) => `rotateX(${degrees}px`;
export const rotateY = (degrees: number) => `rotateX(${degrees}px`;
export const rotateZ = (degrees: number) => `rotateX(${degrees}px`;