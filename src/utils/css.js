export const translateX = (value) => `translateX(${value}px)`;
export const translateY = (value) => `translateY(${value}px)`;
export const translateZ = (value) => `translateZ(${value}px)`;

export const rotateX = (degrees) => `rotateX(${degrees}deg)`;
export const rotateY = (degrees) => `rotateY(${degrees}deg)`;
export const rotateZ = (degrees) => `rotateZ(${degrees}deg)`;